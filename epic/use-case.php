		<p><strong>Name of user & their role:</strong>Jeff is new to jiu jitsu and he wants to have a rotation of gis to use.</p>
		<p><strong>Interaction flow:</strong></p>
		<ul>
			<li>Jeff enters albinoandpreto.com into his web server.</li>
			<li>Server returns Albino and Preto main page.</li>
			<li>Jeff creates a user account.</li>
			<li>Jeff receives confirmation email about his account.</li>
			<li>Jeff confirms account by clicking on confirmation  link in the email.</li>
			<li>Jeff logs into his albinoandpreto.com account</li>
			<li>Jeff clicks on shop and chooses gi he wants</li>
			<li>Server adds gi to his cart</li>
			<li>Jeff clicks on checkout link</li>
			<li>Jeff's shipping information pre-populates</li>
			<li>Jeff has option of entering payment information or choosing PayPal</li>
			<li>Jeff chooses PayPal</li>
			<li>Server redirects Jeff to PayPal.com</li>
			<li>Jeff logs into PayPal account and confirms purchase</li>
			<li>Server redirects Jeff to albinoandpreto.com</li>
			<li>Jeff completes purchase on albinoandopreto.com</li>
		</ul>


